package com.atofighi.bomberman.util;

public class MonsterPackageClass {
    private String name;
    private String content;

    public MonsterPackageClass(String name, String content) {
        this.name = name;
        this.content = content;
    }

    public String getName() {
        return name;
    }

    public String getContent() {
        return content;
    }
}
